package day01;

public class c02_Variables {
    public static void main(String[] args) {

        /*
        java bir runtime programdır
        yani biz yeşil oka bastığımızda   her şey sıfırdan başlar

        classın sonuna kadar tüm kodlar yukarıdan aşağı çalıştırılır
        class bittiğinde her şey sıfırlanır
         */
        int bakiye=0;

        bakiye=200;

        bakiye=500;

        System.out.println( bakiye);

        bakiye=300;

        System.out.println( bakiye );
        bakiye=600;
        System.out.println( bakiye);

    }
}
